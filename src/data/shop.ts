function toTTC(priceHT: number) {
  return Math.round(priceHT * (1 + SHOP_TVA_RATE));
}

function createDocuments(prefix: string): ShopDocument[] {
  return [
    {
      id: `${prefix}-datasheet`,
      title: "Fiche technique",
      type: "PDF",
      size: "1.8 Mo",
    },
    {
      id: `${prefix}-install`,
      title: "Guide d'installation",
      type: "PDF",
      size: "860 Ko",
    },
    {
      id: `${prefix}-warranty`,
      title: "Conditions de garantie",
      type: "PDF",
      size: "420 Ko",
    },
  ];
}

function createVariant(
  id: string,
  label: string,
  sku: string,
  priceHT: number,
  stockStatus: ShopStockStatus = "in_stock",
): ShopVariant {
  return {
    id,
    label,
    sku,
    priceHT,
    priceTTC: toTTC(priceHT),
    stockStatus,
  };
}

function createProduct(
  input: Omit<ShopProduct, "id" | "priceTTC">,
): ShopProduct {
  return {
    ...input,
    id: input.slug,
    priceTTC: toTTC(input.priceHT),
  };
}

